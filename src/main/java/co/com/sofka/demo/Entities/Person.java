package co.com.sofka.demo.Entities;

public class Person
{
    private  String name;
    private  String edad;
    private  String telefono;

    public Person() {
    }

    public String getName() {
        return name;
    }

    public String getEdad() {
        return edad;
    }

    public String getTelefono() {
        return telefono;
    }

    public Person(String name) {
        this.name = name;
    }


}
